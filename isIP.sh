#!/bin/bash
echo -n "Enter you IP: "
read ip
if
[[ "$(echo $ip | grep -E '(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)'| awk -F "." '$1<=255 && $2 <=255 && $3<=255 && $4<=255')" ]]
then
echo "This IP is correct."
else echo "This IP is not correct!"
fi



